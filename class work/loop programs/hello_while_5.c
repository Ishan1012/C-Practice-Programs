/* Program to print Hello World using while loop */
#include<stdio.h>

// Driver code
int main(int argc, char const *argv[])
{
    // Local variable declaration
    int c=0;

    // Printing Hello World 5 times
    while (c<5)
    {
        printf("Hello World\n");
        c++;
    }
    
    return 0;
}
